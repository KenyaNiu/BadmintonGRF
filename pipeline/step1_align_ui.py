def _load_grf(grf_file: Path):
    """
    Load a _grf.npy file with full validation.
    Returns (data_dict, error_str).
    data_dict keys guaranteed: timestamps, fz_combined, plates, metadata, stats
    """
    try:
        raw = np.load(str(grf_file), allow_pickle=True).item()
    except Exception as e:
        return None, f"Cannot load {grf_file.name}: {e}"

    # Validate required keys
    if "timestamps" not in raw:
        return None, "Missing 'timestamps' key in GRF file"
    if "combined" not in raw or "forces" not in raw.get("combined", {}):
        return None, "Missing 'combined.forces' key in GRF file"

    ts = np.asarray(raw["timestamps"], dtype=np.float64)
    f3 = np.asarray(raw["combined"]["forces"], dtype=np.float64)

    if ts.ndim != 1 or len(ts) == 0:
        return None, "timestamps array is empty or malformed"
    if f3.ndim != 2 or f3.shape[1] < 3:
        return None, f"combined.forces shape {f3.shape} unexpected (need Nx3)"

    # Sanitize NaN/Inf
    ts = np.nan_to_num(ts, nan=0.0)
    f3 = np.nan_to_num(f3, nan=0.0, posinf=5000.0, neginf=-5000.0)

    fz = f3[:, 2]  # raw (Vicon sign: contact = negative)

    meta     = raw.get("metadata", {})
    rate     = float(meta.get("analog_rate", 1000.0))
    if rate <= 0:
        rate = 1000.0

    # Per-plate Fz
    plates = {}
    for i in range(1, 5):
        key = f"force_plate_{i}"
        if key in raw and "forces" in raw[key]:
            pf = np.asarray(raw[key]["forces"], dtype=np.float64)
            if pf.ndim == 2 and pf.shape[1] >= 3:
                plates[f"plate{i}"] = np.nan_to_num(pf[:, 2])

    fz_contact = -fz  # contact = positive
    peak_fz    = float(np.max(fz_contact)) if len(fz_contact) else 0.0
    mask_ct    = fz_contact > 200.0
    mean_fz    = float(np.mean(fz_contact[mask_ct])) if mask_ct.any() else 0.0

    return {
        "timestamps":  ts,
        "fz":          fz,          # raw (contact negative)
        "fz_contact":  fz_contact,  # contact positive
        "plates":      plates,
        "rate":        rate,
        "duration":    float(ts[-1]),
        "n_samples":   len(ts),
        "stats": {"peak_fz_N": round(peak_fz, 1), "mean_contact_fz_N": round(mean_fz, 1)},
    }, None

def _downsample(arr, step):
    step = max(1, int(step))
    return arr[::step]

def _stream_video(path: Path, range_header: str):
    try:
        size = path.stat().st_size
    except OSError as e:
        return jsonify({"error": str(e)}), 500
    first, last = 0, size - 1
    if range_header:
        m = re.search(r"bytes=(\d+)-(\d*)", range_header)
        if m:
            first = int(m.group(1))
            last  = int(m.group(2)) if m.group(2) else last
    # clamp
    first = max(0, min(first, size - 1))
    last  = max(first, min(last, size - 1))
    length = last - first + 1
    try:
        with open(path, "rb") as f:
            f.seek(first)
            chunk = f.read(length)
    except OSError as e:
        return jsonify({"error": str(e)}), 500
    rv = Response(chunk, 206, mimetype="video/mp4", direct_passthrough=True)
    rv.headers["Content-Range"]  = f"bytes {first}-{last}/{size}"
    rv.headers["Accept-Ranges"]  = "bytes"
    rv.headers["Content-Length"] = str(len(chunk))
    rv.headers["Cache-Control"]  = "no-cache"
    return rv

