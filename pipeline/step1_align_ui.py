@app.route("/api/sync/<trial>", methods=["POST"])
@_safe
def api_sync_save(trial):
    base, err = _require_base()
    if err: return err

    body = request.get_json(silent=True)
    if not body:
        return jsonify({"error": "Empty request body"}), 400

    grf_t = body.get("grf_event_sec")
    vid_t = body.get("video_event_sec")
    fps   = float(body.get("video_fps", 119.88) or 119.88)
    if fps <= 0: fps = 119.88

    if grf_t is None or vid_t is None:
        return jsonify({"error": "grf_event_sec and video_event_sec are required"}), 400
    if not (isinstance(grf_t, (int, float)) and isinstance(vid_t, (int, float))):
        return jsonify({"error": "event times must be numeric"}), 400

    subj = _subject_of(trial)
    labels_dir = base / subj / "labels"
    labels_dir.mkdir(parents=True, exist_ok=True)
    sp = labels_dir / f"{trial}_sync.json"

    # Preserve existing version history
    versions = []
    if sp.exists():
        try:
            with open(sp, encoding="utf-8") as f:
                old = json.load(f)
            versions = old.get("_versions", [])
            versions.append({
                "offset_sec":    old.get("offset_sec"),
                "annotated_at":  old.get("annotated_at"),
                "quality":       old.get("quality"),
            })
            versions = versions[-5:]  # keep last 5
        except Exception:
            pass

    record = {
        "trial":                 trial,
        "subject":               subj,
        "grf_event_sec":         float(grf_t),
        "video_cam":             int(body.get("video_cam", 2)),
        "video_event_frame":     int(body.get("video_event_frame", 0)),
        "video_fps":             fps,
        "video_event_sec":       float(vid_t),
        "offset_sec":            round(float(grf_t) - float(vid_t), 6),
        "offset_uncertainty_sec": round(1.0 / fps, 6),
        "quality":               body.get("quality", "good"),
        "notes":                 str(body.get("notes", ""))[:500],
        "annotated_at":          datetime.now().isoformat(),
        "tool_version":          "4.0",
        "_versions":             versions,
    }

    # ── Per-camera tracking ──────────────────────────────────────────
    # Merge this camera's annotation into per_cam dict.
    # Load existing per_cam if file already exists.
    per_cam = {}
    if sp.exists():
        try:
            with open(sp, encoding="utf-8") as f:
                old_rec = json.load(f)
            per_cam = old_rec.get("per_cam", {})
        except Exception:
            pass

    cam_key = str(int(body.get("video_cam", 2)))
    per_cam[cam_key] = {
        "video_event_frame": int(body.get("video_event_frame", 0)),
        "video_event_sec":   float(vid_t),
        "offset_sec":        round(float(grf_t) - float(vid_t), 6),
        "annotated_at":      datetime.now().isoformat(),
    }
    record["per_cam"]    = per_cam
    record["cams_done"]  = sorted(int(k) for k in per_cam.keys())

    try:
        tmp = sp.with_suffix(".tmp")
        with open(tmp, "w", encoding="utf-8") as f:
            json.dump(record, f, indent=2, ensure_ascii=False)
        tmp.replace(sp)  # atomic rename
    except OSError as e:
        return jsonify({"error": f"Cannot write sync file: {e}"}), 500

    return jsonify({
        "ok":                    True,
        "offset_sec":            record["offset_sec"],
        "offset_uncertainty_sec": record["offset_uncertainty_sec"],
        "path":                  str(sp),
    })

