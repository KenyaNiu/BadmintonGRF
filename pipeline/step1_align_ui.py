def _fwd(p) -> str:
    """Return a Path as a forward-slash string (safe for JSON / JS)."""
    return str(p).replace("\\", "/")

@app.route("/api/browse")
@_safe
def api_browse():
    """Server-side directory browser for root picking."""
    path = request.args.get("path", "").strip()

    # No path → list drives
    if not path:
        drives = _list_drives()
        return jsonify({
            "path":    "",
            "entries": [{"name": d, "path": d, "type": "drive"} for d in drives],
            "parent":  None,
            "has_subjects": False,
        })

    # Normalize bare Windows drive letter "F:" → "F:/" so Path() resolves correctly
    if re.match(r'^[A-Za-z]:$', path):
        path = path + "/"

    try:
        p = Path(path).resolve()
        if not p.exists():
            return jsonify({"error": f"Does not exist: {_fwd(p)}"}), 404
        if not p.is_dir():
            return jsonify({"error": f"Not a directory: {_fwd(p)}"}), 400

        entries = []
        try:
            children = sorted(p.iterdir(), key=lambda x: x.name.lower())
        except PermissionError:
            return jsonify({"error": "Permission denied"}), 403

        for child in children:
            if not child.is_dir():
                continue
            is_sub = bool(re.match(r'^sub_\d+$', child.name))
            entries.append({
                "name":       child.name,
                "path":       _fwd(child),   # always forward slashes
                "type":       "subject" if is_sub else "dir",
                "is_subject": is_sub,
            })
            if len(entries) >= 200:
                break

        n_subs     = sum(1 for e in entries if e["is_subject"])
        parent_str = _fwd(p.parent) if p != p.parent else None

        return jsonify({
            "path":          _fwd(p),        # always forward slashes
            "parent":        parent_str,
            "entries":       entries,
            "has_subjects":  n_subs > 0,
            "subject_count": n_subs,
        })
    except Exception as e:
        return jsonify({"error": str(e)}), 500

